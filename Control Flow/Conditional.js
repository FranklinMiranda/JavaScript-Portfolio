// If, Else If, and Else Statements 
const num = 10;
const num1 = 20;

if (num + num1 > 30) {
  console.log(num + num1, 'greater than 30');
} else if (num + num1 === 30) {
  console.log(num + num1);
} else {
  console.log(num + num1, 'less than 30');
}

